// Switch Statement

let ch = 4;

switch(ch)
{
  case 1:
    console.log("I am 1");
    break;
    case 2:
        console.log("I am 2");
        break;
        case 3:
            console.log("I am 3");
            break;
            default:
                console.log("You select a wrong choice");
                break;
}